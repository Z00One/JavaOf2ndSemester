
public class DataType1 {

	public static void main(String[] args) {
		char a = '1';
		long foo = 1;
		final int temp = 3; //temp is a constant
		// temp is General Constant
		// '1',3 is Literal Constant
		// 리터럴은 이름 없는 상수 이름으로 호출이 불가
		// 값 자체로 사용한다.
		
		short bar = 3;
		
		int fooo = 0x00000011;
		System.out.println(fooo);
	}

}
